class player:
    def __init__(self):
        self.x = random.randint(5 ,10) * 50
        self.y = random.randint(7 ,12) * 50
        self.point = 0

    def draw(self):
        screen.blit(ghost_image, (self.x, self.y))

    def move(self):
        if pressed_keys[K_RIGHT] and (self.x_key != (2 or 3)):
            self.x += 2
        if pressed_keys[K_LEFT] and (self.x_key != (1 or 3)):
            self.x -= 2
        if pressed_keys[K_UP] and (self.y_key != (2 or 3)):
            self.y -= 2
        if pressed_keys[K_DOWN] and (self.y_key != (1 or 3)):
            self.y += 2

    def positioning(self):
        self.x_key = 0
        self.y_key = 0

        if a[int(self.y / 50)][int(self.x / 50)] == 0:
                self.point += 1
        if a[int(self.y // 50)][int(round(self.x / 50))] == 1:
                self.y_key = 2
        if a[int(self.y // 50) + 1][int(round(self.x / 50))] == 1:
            if self.y_key == 2:
                 self.y_key = 3
            else:
                 self.y_key = 1
        if a[int(round(self.y / 50))][int(self.x // 50) + 1] == 1:
            self.x_key = 2
        if a[int(round(self.y / 50))][int(self.x // 50)] == 1:
            if self.x_key == 2:
                self.x_key = 3
            else:
                self.x_key = 1







